;累加并以二进制输出

DATA SEGMENT
NUM DW 1122H
    DW 3344H
    DW 5566H
DATA ENDS

STACK SEGMENT USE16
    DB 100 DUP(?)
STACK ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: 

    MOV SI,offset NUM
    CALL COMPUTE
XYZ:
    CALL DISP
    MOV AH,4CH
    INT 21H

COMPUTE PROC
    MOV BX,0
    ADD BX,[SI]
    ADD BX,[SI+2]
    ADD BX,[SI+4]
    RET
COMPUTE ENDP

DISP PROC
    MOV CX,16
LIST:
    MOV DL,"0"
    RCL BX,1
    JNC NEXT
    MOV DL,"1"
NEXT:
    MOV AH,2H
    INT 21H
    LOOP LIST
    RET
DISP ENDP

CODE ENDS
END START